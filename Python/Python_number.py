#BASIC ARITHMETIC: 
#Addition: 
print(2+1)

#Subtraction:
print(2-1)

#Multiplication:
print(2*2)

#Division:
print(3/2)

#Floor Division:
print(7//4)
    #The reason we get this result is because we are using "floor" division. 
    # The // operator (two forward slashes) truncates the decimal without rounding, 
    # and returns an integer result.

#Modulo: When we just want the remainder after division
print(7%4)

#Powers:
print(16**2)

#Root:
print(4**0.5)





